// Search function with fuzzy matching
export function searchSite(query) {
    if (!query || query.trim().length < 2) {
        return [];
    }

    const searchTerm = query.toLowerCase().trim();
    const results = [];

    searchData.forEach((item) => {
        let score = 0;

        // Exact title match (highest priority)
        if (item.title.toLowerCase() === searchTerm) {
            score += 100;
        }
        // Title starts with search term
        else if (item.title.toLowerCase().startsWith(searchTerm)) {
            score += 50;
        }
        // Title contains search term
        else if (item.title.toLowerCase().includes(searchTerm)) {
            score += 30;
        }

        // Description match
        if (item.description.toLowerCase().includes(searchTerm)) {
            score += 20;
        }

        // Keywords match
        item.keywords.forEach((keyword) => {
            if (keyword === searchTerm) {
                score += 40;
            } else if (keyword.includes(searchTerm) || searchTerm.includes(keyword)) {
                score += 15;
            }
        });

        // Category match
        if (item.category.toLowerCase().includes(searchTerm)) {
            score += 10;
        }

        if (score > 0) {
            results.push({ ...item, score });
        }
    });

    // Sort by score (highest first) and return top 8 results
    return results
        .sort((a, b) => b.score - a.score)
        .slice(0, 8);
}
